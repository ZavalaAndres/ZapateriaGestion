        consulta1.add(jTextField2.getText());
        consulta1.add(jTextField1.getText());
        Insercion ins=new Insercion("Empresa",consulta1);
        ins.inserta();
        try {
            DatabaseConnection c=DatabaseConnection.getInstance();
            PreparedStatement ps=c.getConnection().prepareStatement("insert into direccion "
                    + "(nombreEmpresa,calle,colonia,numExt,cp) values (?,?,?,?,?)");
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jTextField3.getText());
            ps.setString(3, jTextField6.getText());
            ps.setInt(4, Integer.parseInt(jTextField4.getText()));
            ps.setString(5, jTextField5.getText());
            ps.execute();
            ps.close();
            this.dispose();
            if(proveedores)
                JOptionPane.showMessageDialog(null,"Ahora intente insertar usuario!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: "+e);
        }
        
        
